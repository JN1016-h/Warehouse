					gongyingshangid: [
					],
					gongyingshangbianhao: [
						{ required: true, message: '供应商编号不能为空', trigger: 'blur' },
					],
					gongyingshangmingcheng: [
					],
					dizhi: [
					],
					lianxidianhua: [
					],
					fuzeren: [
