				ruleForm: {
					gongyingshangid: '',
					gongyingshangbianhao: '',
					gongyingshangmingcheng: '',
					dizhi: '',
					lianxidianhua: '',
					fuzeren: '',
					gongyingshangxiangqing: '',
					tianjiashijian: '',
				},
		

